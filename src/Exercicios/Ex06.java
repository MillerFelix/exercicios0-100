package Exercicios;

public class Ex06 {

	public static void main(String[] args) {

		// Ler dois números inteiros e exibir o quociente e o resto da divisão inteira
		// entre eles.

		int num1 = 8;
		int num2 = 12;
		double aliquota = num1 / num2;
		double resto = num1 % num2;
		System.out.println("O resto da divisao é: " + resto);
		System.out.println("A aliquota é: " + aliquota);
	}

}
